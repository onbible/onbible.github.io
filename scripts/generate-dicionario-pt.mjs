function letterKey(termo) {
  const c = termo.normalize('NFD').replace(/[\u0300-\u036f]/g, '').toLowerCase().charAt(0);
  return c >= 'a' && c <= 'z' ? c : null;
}

const byLetter = {};
for (const L of 'abcdefghijklmnopqrstuvwxyz'.split('')) {
  byLetter[L] = [];
}

const seen = new Set();
for (const row of SEED) {
  const [termo, definicoes] = row;
  const lk = letterKey(termo);
  if (!lk) continue;
  const k = `${lk}:${termo.toLowerCase()}`;
  if (seen.has(k)) continue;
  seen.add(k);
  byLetter[lk].push({ termo, definicoes });
}

for (const L of Object.keys(byLetter)) {
  byLetter[L].sort((a, b) => a.termo.localeCompare(b.termo, 'pt'));
}

const lista = {};
for (const L of 'abcdefghijklmnopqrstuvwxyz'.split('')) {
  lista[L] = [L];
  const upper = L.toUpperCase();
  const payload = { [upper]: byLetter[L] };
  mkdirSync(join(OUT, L), { recursive: true });
  writeFileSync(join(OUT, L, `${L}.json`), JSON.stringify(payload, null, 2), 'utf8');
}

writeFileSync(join(OUT, 'lista_letras.json'), JSON.stringify(lista, null, 2), 'utf8');

console.log('Gerado db/dicionario_pt com', SEED.length, 'linhas de seed (deduplicadas por letra).');
